async function main() {
  let creadas = 0;
  let existentes = 0;

  for (const nombre of actividades) {
    const actividadExistente = await prisma.actividad.findFirst({
      where: {
        nombre,
      },
    });

    if (actividadExistente) {
      existentes++;
      continue;
    }

    await prisma.actividad.create({
      data: {
        nombre,
        activo: true,
      },
    });

    creadas++;
  }

  console.log("Carga de actividades finalizada");
  console.log(`Actividades reales: ${actividades.length}`);
  console.log(`Creadas: ${creadas}`);
  console.log(`Ya existentes: ${existentes}`);
}

main()
  .catch((error) => {
    console.error("Error cargando actividades:", error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });